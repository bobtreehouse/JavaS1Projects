package com.company;

public class Pop {

    String brandName;
    String flavor;
    boolean isDiet;
    boolean hasFructose;
    int calories;

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getFlavor() {
        return flavor;
    }

    public void setFlavor(String flavor) {
        this.flavor = flavor;
    }

    public boolean isDiet() {
        return isDiet;
    }

    public void setDiet(boolean diet) {
        isDiet = diet;
    }

    public boolean isHasFructose() {
        return hasFructose;
    }

    public void setHasFructose(boolean hasFructose) {
        this.hasFructose = hasFructose;
    }

    public int getCalories() {
        return calories;
    }

    public void setCalories(int calories) {
        this.calories = calories;
    }




}
