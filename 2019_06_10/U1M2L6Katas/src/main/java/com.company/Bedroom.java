package com.company;

public class Bedroom {

    private int squareFootage;
    private int length;
    private int width;
    private String floorType;
    private String extraStuff;
    private boolean hasSunlight;

    public int getSquareFootage() {
        return squareFootage;
    }

    public void setSquareFootage(int squareFootage) {
        this.squareFootage = squareFootage;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public String getFloorType() {
        return floorType;
    }

    public void setFloorType(String floorType) {
        this.floorType = floorType;
    }

    public String getExtraStuff() {
        return extraStuff;
    }

    public void setExtraStuff(String extraStuff) {
        this.extraStuff = extraStuff;
    }

    public boolean isHasSunlight() {
        return hasSunlight;
    }

    public void setHasSunlight(boolean hasSunlight) {
        this.hasSunlight = hasSunlight;
    }



}
