package com.company;

public class Mints {

    public String type;
    public String brand;
    public boolean hasNutraSweet;
    int qtyPerPack;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public boolean isHasNutraSweet() {
        return hasNutraSweet;
    }

    public void setHasNutraSweet(boolean hasNutraSweet) {
        this.hasNutraSweet = hasNutraSweet;
    }

    public int getQtyPerPack() {
        return qtyPerPack;
    }

    public void setQtyPerPack(int qtyPerPack) {
        this.qtyPerPack = qtyPerPack;
    }
}
