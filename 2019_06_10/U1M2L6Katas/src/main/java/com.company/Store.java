package com.company;

public class Store {

    private String address;
    private String name;
    private int numEmployees;
    private boolean fireCodeApproved;
    private boolean healthApproved;
    private int numSmokeDetectors;
    private int squareFeet;
    private int numFloors;


    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumEmployees() {
        return numEmployees;
    }

    public void setNumEmployees(int numEmployees) {
        this.numEmployees = numEmployees;
    }

    public boolean isFireCodeApproved() {
        return fireCodeApproved;
    }

    public void setFireCodeApproved(boolean fireCodeApproved) {
        this.fireCodeApproved = fireCodeApproved;
    }

    public boolean isHealthApproved() {
        return healthApproved;
    }

    public void setHealthApproved(boolean healthApproved) {
        this.healthApproved = healthApproved;
    }

    public int getNumSmokeDetectors() {
        return numSmokeDetectors;
    }

    public void setNumSmokeDetectors(int numSmokeDetectors) {
        this.numSmokeDetectors = numSmokeDetectors;
    }

    public int getSquareFeet() {
        return squareFeet;
    }

    public void setSquareFeet(int squareFeet) {
        this.squareFeet = squareFeet;
    }

    public int getNumFloors() {
        return numFloors;
    }

    public void setNumFloors(int numFloors) {
        this.numFloors = numFloors;
    }



}
